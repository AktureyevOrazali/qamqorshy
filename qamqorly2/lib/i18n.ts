import { cookies } from 'next/headers'

export type Lang = 'ru' | 'en' | 'kz'

export async function getLangFromCookies(): Promise<Lang> {
  const store = await cookies()
  const lang = store.get('lang')?.value
  if (lang === 'ru' || lang === 'en' || lang === 'kz') {
    return lang
  }
  return 'ru'
}

